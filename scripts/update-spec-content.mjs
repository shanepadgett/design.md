import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const specDir = path.join(process.cwd(), "docs", "specs");
const outputPath = path.join(process.cwd(), "src", "core", "spec", "spec-content.ts");

const humanFiles = [
  "README.md",
  "document-structure.md",
  "token-syntax.md",
  "token-sections.md",
  "linting.md",
  "exporting.md",
];

const humanSpec = await readSpecs(humanFiles);
const agentSpec = await readSpecs(["agent.md"]);

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(
  outputPath,
  `// Generated by scripts/update-spec-content.mjs. Do not edit directly.\n\nexport const humanSpec = ${JSON.stringify(humanSpec)};\n\nexport const agentSpec = ${JSON.stringify(agentSpec)};\n`,
  "utf8",
);

async function readSpecs(files) {
  const parts = [];

  for (const file of files) {
    const content = await readFile(path.join(specDir, file), "utf8");
    parts.push(content.trimEnd());
  }

  return `${parts.join("\n\n---\n\n")}\n`;
}
